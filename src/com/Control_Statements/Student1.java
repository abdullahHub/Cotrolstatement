package com.Control_Statements;

public class Student1 implements Cloneable {
	public static void main(String[]args) {
		int num = 2;
		switch(num) {
		case 3:
			System.out.println("Number is 3");
			break;
		case 1:
			System.out.println("Number is 1");
			break;
			default:
				System.out.println(num);
		}
	}
}
